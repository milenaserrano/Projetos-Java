    private void BtnLocalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnLocalizarActionPerformed
        String VCodigo = JOptionPane.showInputDialog("Informe o CÓDIGO do grupo!!!");
    
        if(VCodigo.equals("")){
            JOptionPane.showMessageDialog (null, "Favor informar o CÓDIGO do grupo!!!");
            return;
    
        }
        try {
            String pesquisa = "select * from grupos where codigo =" + VCodigo;
            con_bd.executaSQL(pesquisa);
            if (con_bd.resultset.first()) {
                TxtCodigo.setText(con_bd.resultset.getString("Codigo"));
                TxtGrupo.setText(con_bd.resultset.getString("Grupo"));
                TxtCodigo.setEnabled(false);
                BtnIncluir.setEnabled(false);
                BtnAlterar.setEnabled(true);
                BtnExcluir.setEnabled(true);
            } else {
             JOptionPane.showMessageDialog (null, "\nNão existe grupo cadastrado com o código informado !!!");
            
        }
        } catch (SQLException erro) {
            JOptionPane.showMessageDialog (null, "\nProblema no acesso aos dados! "+erro.getMessage());
            
        }
    }//GEN-LAST:event_BtnLocalizarActionPerformed

    private void BtnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAlterarActionPerformed
      if (TxtGrupo.getText().equals("")){
           JOptionPane.showMessageDialog (null, "Grupo de produtos deve possuir no máximo 15 caracteres !!! ");
          TxtGrupo.requestFocus();
          return;
      }
      if (TxtGrupo.getText().length()>15){
           JOptionPane.showMessageDialog (null, "Grupo de produtos deve possuir no máximo 15 caracteres !!! ");
           TxtGrupo.requestFocus();
            return;
      }
      String varsql = "update grupos set grupo='" + TxtGrupo.getText() + "' where codigo =" + TxtCodigo.getText();
      try {
          con_bd.statement.executeUpdate(varsql);
          JOptionPane.showMessageDialog (null, "Dados(s) alterados(s) com sucesso !!! ");
          
          Limp_Camp();
      }catch (SQLException ex){
           JOptionPane.showMessageDialog (null, "Código já cadastrado!!! ", "Alteração", JOptionPane.INFORMATION_MESSAGE );
           TxtCodigo.requestFocus();
          
          
      }
    
    }//GEN-LAST:event_BtnAlterarActionPerformed

    private void BtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnExcluirActionPerformed
       String sql = "";
       try {
           int resposta = JOptionPane.showConfirmDialog(rootPane,"Deseja excluir esse grupo ?","Confirmar Exclusão", JOptionPane.YES_NO_OPTION,3);
           if(resposta ==0){
               sql = "delete from grupos where codigo = " + TxtCodigo.getText();
                con_bd.statement.executeUpdate(sql);
                 JOptionPane.showMessageDialog(rootPane,"Exclusão realizada com sucesso!");
                 Limp_Camp();
           }
       }catch (SQLException erro){
             JOptionPane.showMessageDialog (null, "\nErro na exclusão!\nErro:" + erro +"\nComando SQL passado:" + sql);
           
                
               
           
       }
    }//GEN-LAST:event_BtnExcluirActionPerformed
       public void  Limp_Camp(){ 
           TxtCodigo.setText("");
           TxtGrupo.setText("");
           TxtCodigo.setEnabled(true);
           TxtCodigo.requestFocus();
           BtnIncluir.setEnabled(true);
           BtnAlterar.setEnabled(false);
           BtnExcluir.setEnabled(false);
       }

