    private void BtnIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnIncluirActionPerformed
    int VCodigo=0;
    try {
        VCodigo = Integer.parseInt(TxtCodigo.getText());
    }
    catch (NumberFormatException erro) {
        JOptionPane.showMessageDialog(null, "Favor informar um código do grupo válido!!!");
        TxtCodigo.requestFocus();
        return; 
    }
    if (VCodigo <=0){
        JOptionPane.showMessageDialog(null, "Código do grupo deve ser maior ou igual a 0!!!");
        TxtCodigo.requestFocus();
        return;
    }
    
    if(TxtGrupo.getText().equals("")){
        JOptionPane.showMessageDialog(null, "Favor informar o grupo de produtos!!");
        TxtGrupo.requestFocus();
        return;   
    }
    
    if(TxtGrupo.getText().length()>15){
        JOptionPane.showMessageDialog(null, "Grupo de produtos deve possuir no máximo 15 caracteres!!!");
        TxtGrupo.requestFocus();
        return;
  
    }//GEN-LAST:event_BtnIncluirActionPerformed
 
    String insert_sql = "insert into grupos(codigo, grupo) values (" + TxtCodigo.getText() +",'" + TxtGrupo.getText() + "')";
    try {
    con_bd.statement.executeUpdate(insert_sql);
    JOptionPane.showMessageDialog(null, "Grupo cadastrado com sucesso!");
    Limp_Camp();
  } catch (SQLException ex){
    JOptionPane.showMessageDialog (null, "Código já cadastrado!!","Inclusão", JOptionPane.INFORMATION_MESSAGE);
    TxtCodigo.requestFocus();
    }
  }
  
